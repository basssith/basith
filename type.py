# Different types of variables
integer_value = 42
float_value = 3.14
string_value = "Hello, World!"
list_value = [1, 2, 3]
dictionary_value = {"key": "value"}

# Using type() to check the types
print("The type of integer_value is:", type(integer_value))
print("The type of float_value is:", type(float_value))
print("The type of string_value is:", type(string_value))
print("The type of list_value is:", type(list_value))
print("The type of dictionary_value is:", type(dictionary_value))
